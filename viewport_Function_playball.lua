function viewport.playball ( )
--------------------------------------------------------------------------------
	
    local hplayer1 = this.getUser ( )
    local hscene = user.getScene ( hplayer1 )
    local hplayball = scene.createRuntimeObject ( hscene, "playball"  )

       
    if ( hplayball ~= nil )
    then
    object.setTranslation ( hplayball ,5,10,5,object.kGlobalSpace )
    end 
	
--------------------------------------------------------------------------------
end
